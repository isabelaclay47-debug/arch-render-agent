#!/bin/bash
# 建筑渲染智能体 — Linux 停止常驻服务
cd "$(dirname "$0")" || exit 1
echo "============================================"
echo "   停止建筑渲染智能体常驻服务"
echo "============================================"
echo
# 写入停止标志：守护进程每秒巡检一次，会在 1-2 秒内优雅关闭 app.py 并退出。
: > ".supervisor_stop"
echo "已发送停止指令，服务将在几秒内停止..."
sleep 4
# 兜底：守护进程没在跑时标志不会被消费，这里清掉它，避免下次启动被误判为"要求停止"。
[ -f ".supervisor_stop" ] && rm -f ".supervisor_stop"
echo "完成。"
