def qc_and_revise_prompt(iteration: int) -> str:
    """把生成结果发回导演对话：查篡改 + 判断下一步走"精修"还是"重画" + 给出对应修改依据。

    关键：不再只会"改提示词重画"。局部瑕疵改走"在这张图基础上增量精修"，
    只有形体被明显篡改或需要全局大改时才从原图重画——省额度，且缺陷能真正收敛。
    """
    return f"""这是第 {iteration} 轮的结果（第一张为忠实性参照的原图底图，第二张为本轮生成图）。请执行三步：

第一步【篡改检查】逐项对比生成图与原图底图：
- 建筑形体/轮廓/层数、开窗位置与比例是否一致？有没有凭空增删体块、构件、楼层？
- 视角与透视是否偏移？特殊构件（格栅/连廊/坡道等）是否保留？
- 图中文字（标识/门头/指示牌等）是否清晰端正？有没有被扭曲、变形或写成乱码？
- 画质评价：光影是否自然、材质是否真实、配景是否得当。

第二步【判断下一步怎么走】——这一步决定省不省额度：
- 选「精修」：如果建筑形体与原图基本一致，剩下的主要是**局部瑕疵**（个别文字/某处材质/某个构件/局部配景/某处光影）。此时应在本轮这张图基础上只改这些局部，**不要推倒重画**——已经画好的部分逐像素保留，既省额度又不会把上一轮的好效果弄丢。
- 选「重画」：仅当**建筑形体被明显篡改**（层数/开窗/体块变了、透视偏了），或主要问题是**全局性的**（整体光线/氛围/构图方向不对，非大改不可）时，才从原图底图按修订后的完整提示词重画。

第三步【给出修改依据】
- 若选「精修」：列出这张图上**具体待修的局部缺陷清单**（用于在这张图基础上做增量修改，其余部分保持不变）；每条聚焦一个部位、说清怎么改。
- 若选「重画」：给出完整的下一轮提示词（被篡改的部位用明确语言锁定、画质不足处加强描述），末尾含忠实性约束。

请严格按以下格式输出，不要输出其他内容：
<分析>
（篡改点与画质问题的逐条清单，每条一行，格式：[严重/中等/轻微] 问题描述）
</分析>
<忠实度>一致 | 轻微偏移 | 明显篡改</忠实度>
<下一步>精修 | 重画</下一步>
<结论>
（一句话：本轮是否可接受，最大的问题是什么）
</结论>
<精修指令>
（仅当下一步为「精修」时填：本轮图上待修的局部缺陷清单，每条一行；否则留空）
</精修指令>
<新提示词>
（仅当下一步为「重画」时填：完整的下一轮提示词，末尾含忠实性约束；否则留空）
</新提示词>"""


