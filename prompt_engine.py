import re


def parse_director_reply(text: str) -> dict:
    """解析导演对话的 <分析>/<结论>/<新提示词> 结构，宽容缺段。"""
    def grab(tag: str) -> str:
        m = re.search(rf"<{tag}>(.*?)</{tag}>", text, re.S)
        return m.group(1).strip() if m else ""

    analysis = grab("分析")
    verdict = grab("结论")
    new_prompt = grab("新提示词")
    fidelity = grab("忠实度")            # 一致 / 轻微偏移 / 明显篡改
    next_step = grab("下一步")           # 精修 / 重画
    refine_instruction = grab("精修指令")
    found_any = any([analysis, verdict, new_prompt, fidelity, next_step,
                     refine_instruction])
    if not new_prompt and not found_any:
        # 模型完全没按标签输出：取最后一个标签后的全部内容兜底当提示词
        tail = re.split(r"</?[^>]+>", text)[-1].strip()
        if len(tail) > 80:
            new_prompt = tail
    return {"analysis": analysis, "verdict": verdict, "new_prompt": new_prompt,
            "fidelity": fidelity, "next_step": next_step,
            "refine_instruction": refine_instruction}


def extract_questions(text: str) -> str:
    """提取导演对话的 <反问> 内容；没有反问时返回空串。"""
    m = re.search(r"<反问>(.*?)</反问>", text, re.S)
    return m.group(1).strip() if m else ""
