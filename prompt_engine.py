# 忠实性硬约束——只写模型默认不保证的事，不堆"保持原始比例"这类自带行为
FIDELITY_RULES = """【忠实性约束】
1. 建筑的形体、层数、开窗和标志性构件（格栅/连廊/坡道等）必须与原图完全一致，不得增删或变形；
2. 图中所有文字（标识、门头、指示牌、幕墙logo等）必须保持原有内容，字形清晰端正，绝不允许扭曲、变形或变成乱码；无法写清楚的文字宁可保持原样不动；
3. 只提升：材质真实感、光影、天空、配景（人/树/车）和画面质感。"""

